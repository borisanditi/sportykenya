interface SocialMediaLinksProps {
    variant?: 'footer' | 'floating';
    className?: string;
}

export function SocialMediaLinks({ variant = 'footer', className }: SocialMediaLinksProps) {
    if (variant === 'floating') {
        return (
            <div className={cn("hidden md:flex flex-col gap-4 fixed left-4 top-1/2 -translate-y-1/2 z-50", className)}>
                {socialLinks.map((link) => (
                    <Link
                        key={link.name}
                        href={link.url}
                        target="_blank"
                        rel="noopener noreferrer"
                        className={cn(
                            "group flex items-center justify-center p-3 bg-white rounded-full shadow-lg transition-transform hover:scale-110",
                            link.hoverColor,
                            "text-gray-700"
                        )}
                        title={link.name}
                    >
                        <link.icon className="w-6 h-6" />
                    </Link>
                ))}
            </div>
        );
    }

    // Footer variant
    return (
        <div className={cn("flex space-x-6", className)}>
            {socialLinks.map((link) => (
                <Link
                    key={link.name}
                    href={link.url}
                    target="_blank"
                    rel="noopener noreferrer"
                    className={cn(
                        "text-gray-400 hover:text-gray-300 transition-colors",
                        // In footer, we usually want white/gray icons that might light up on hover, or just stay subtle.
                        // The user asked for "relevant icons". I will make them colored on hover.
                        link.hoverColor.replace('text-', 'text-')
                    )}
                >
                    <span className="sr-only">{link.name}</span>
                    <link.icon className="h-6 w-6" aria-hidden="true" />
                </Link>
            ))}
        </div>
    );
}
